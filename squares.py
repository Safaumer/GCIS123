import turtle
def side():
    turtle.bgcolor("pink")
    turtle.fillcolor("blue")
    turtle.begin_fill()
    turtle.pencolor("red")
    turtle.pensize(10)
    turtle.speed(8)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.end_fill()
side()

